public class Animal {
    private String nome;
    private String tamanho;
    private String cor;

    public String getNome(){
        return this.nome;
    }
    public String getTamnho(){
        return this.tamanho;
    }
    public String getCor(){
        return this.cor;
    }

    public void setNome(String nomeNovo){
        this.nome = nomeNovo;
    }
    public void setTamanho(String tamanhoNovo){
        this.tamanho = tamanhoNovo;
    }
    public void setCor(String corNovo){
        this.cor = corNovo;
    }











}